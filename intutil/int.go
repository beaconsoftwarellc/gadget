package intutil

// Int64Max returns the in value or max whichever is smaller
func Int64Max(in, max int64) int64 {
	if in > max {
		return max
	}
	return in
}

// Int64Min returns the in value or min whichever is larger
func Int64Min(in, min int64) int64 {
	if in < min {
		return min
	}
	return in
}

